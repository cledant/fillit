#include <string.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*loc;

	loc = malloc(size);
	if (loc != NULL)
		ft_bzero(loc, size);
	return (loc);
}
