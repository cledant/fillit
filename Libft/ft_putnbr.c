#include <string.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	int tmp;

	tmp = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	tmp = n % 10;
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(tmp + '0');
}
